package chap9ArraysStringsAndVectors;

public class ArrayData {
    
    public static void main(String[] args) {
        
        String[] stringArray = {"Java", "Visual Basic", "VC++", "C"};
        
        for (int i = 0; i < stringArray.length; i++) {
            System.out.print(stringArray[i] + ", ");
            
        }
    }

}
